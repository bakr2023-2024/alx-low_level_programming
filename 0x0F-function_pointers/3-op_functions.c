#include "3-calc.h"
/**
 * op_add - ambola
 * @a: kaka
 * @b: bibi
 * Return: kaka
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - ambola
 * @a: kaka
 * @b: bibi
 * Return: bibi
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - ambola
 * @a: kaka
 * @b: bibi
 * Return: ambola
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - ambola
 * @a: kaka
 * @b: bibi
 * Return: tesa
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - ambola
 * @a: kaka
 * @b: bibi
 * Return: ofa
 */
int op_mod(int a, int b)
{
	return (a % b);
}
